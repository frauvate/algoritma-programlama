package sinavsorulari.mathfunction;

public class MatFonk {
    public static int sbtsayi = 4;

    MatFonk() {
        sbtsayi = 400;
    }

    public static int bulKare(int deger3) {
        return deger3 * deger3;
    }

    public static int bulKup(int deger2) {
        return deger2 * deger2 * deger2;
    }

    public static double bulKarekok(double deger1) {
        return Math.sqrt(deger1);
    }
}
